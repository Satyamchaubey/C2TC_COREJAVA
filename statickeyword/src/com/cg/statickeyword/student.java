package com.cg.statickeyword;

public class student {
	
	
	private String name;
	private long mobile;
	private String email;
	private String college;
	private float cgppa;
	public student(String name, long mobile, String email, String college, float cgppa) {
		super();
		this.name = name;
		this.mobile = mobile;
		this.email = email;
		this.college = college;
		this.cgppa = cgppa;
	}
	


}
