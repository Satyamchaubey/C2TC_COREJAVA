package cg.com.controlestatements;

public class IfStatement {
	
//	if-statement--->>
	
//    1-Evaluates a condition
//    2-controls based on the condition result
//    3-either true or false
//    4-Four types
//                 (i) if
//                 (ii) if-else
//                 (iii) else-if
//                 (iv) nested if
//                                    syntax: 
//                                 	   if(<condition>) {
//                                 		   block of code
//                                 	   }

	public static void main(String[] args) {
		int x = 10;
		int y = 12;
		if(x+y > 20) {
			System.out.println("x+y is greater than 20");
		}
		
	}

}
